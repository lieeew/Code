import java.util.*;
public class TestIf04{
	public static void main(String[] arges){
		Scanner sc = new Scanner(System.in);
		System.out.println("请输入您的会员积分:");
	if(  sc.hasNextInt() == true ){  // 加括号!!!
		 int soccer = sc.nextInt() ; // 加括号!!!
		 double  discount = 0.1;  //这个有范围,写在某一个if里面会报错
		if (soccer > 8000){
			discount = 0.6;
		}else if (soccer >= 4000){
			discount = 0.7;
		}else if (soccer >= 2000 ){
			discount = 0.8;
		}else {
			discount = 0.9;
		}System.out.println("您的折扣为:"+discount);
		
	}else {
		System.out.println("请输入整数");
	}
	}
}
	
